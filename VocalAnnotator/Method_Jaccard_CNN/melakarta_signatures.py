def get_allowed_swaras(raga_name):
    """
    Returns a set of allowed swara strings (across 3 octaves) for a given Raga.
    """
    base = MELAKARTA_RAGAS.get(raga_name, set())
    if not base: return None
    
    full_list = set()
    # Middle Octave
    full_list.update(base)
    # Lower Octave (suffix '_')
    full_list.update({s + "_" for s in base})
    # Upper Octave (suffix "'")
    full_list.update({s + "'" for s in base})
    
    return full_list
